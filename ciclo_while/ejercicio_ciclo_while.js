let miArreglo = []; //Declaramos un arreglo vacio
let i = 0; //Declaramos la varible i y la inicializamos en cero

console.log(miArreglo); //Mostramos el contenido el arreglo antes del bucle while

//Ciclo while
while(i < 10){
  miArreglo.push(i + 1);
  i++
};

console.log(miArreglo) // Mostramos el contenido de arreglo despues del bucle while

